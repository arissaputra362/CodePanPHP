<p>Teknologi firewall adalah salah satu dari sekian banyak sistem keamanan yang dapat diterapkan pada jaringan private
    internet. Firewall ini sendiri merupakan perangkat keamanan jaringan yang memantau lalu lintas jaringan yang masuk
    dan keluar. Firewall yang akan memutuskan apakah akan mengizinkan atau memblokir lalu lintas tertentu berdasarkan
    seperangkat aturan keamanan yang ditentukan. Maka dari itu sangat penting memiliki teknologi firewall dalam jaringan
    internet, terutama untuk jaringan internal perusahaan.</p>

<p>
    Adapun jenis firewall dapat dibagi menjadi beberapa kategori berbeda berdasarkan struktur umum dan metode
    operasinya, antara lain: <br><br>

    <b>1. Packet-Filtering Firewall <br><br></b>

    Packet-Filtering Firewall adalah sebuah mekanisme yang bisa memblokir paket-paket data jaringan yang dilakukan
    berdasarkan aturan yang telah ditentukan sebelumnya oleh user. Dengan teknologi keamanan ini, paket data akan
    disortir untuk bisa masuk dan yang akan diblokir. <br><br>

    Lalu lintas jaringan yang mencurigakan seperti alamat IP yang tidak dikenal, nomor TCP/UDP yang aneh, aplikasi yang
    menurut program membahayakan, dan sebagainya akan diblokir oleh Filter Firewall.<br><br>

    Karena ini merupakan jenis firewall yang paling tua alias pertama ada, cara kerjanya pun paling sederhana. Firewall
    akan memeriksa paket data yang berseliweran di traffic router atau traffic switch.<br><br>

    Kelebihan Packet-Filtering Firewall adalah, tidak membutuhkan sumber daya yang banyak, yang berarti tidak berdampak
    besar pada kinerja komputer Anda. Kekurangannya, firewall ini kurang akurat dibandingkan firewall baru yang
    teknologinya sudah pasti lebih canggih.<br><br>

    <b>2. Circuit-Level Gateway<br><br></b>

    Circuit-Level Gateway adalah jenis firewall sederhana lainnya yang dimaksudkan untuk kerja cepat dan mudah dalam
    menyetujui atau menolak lalu lintas, tanpa menggunakan sumber daya komputasi yang signifikan.<br><br>

    Fungsi utama firewall ini adalah memverifikasi Transmission Control Protocol (TCP). Pemeriksaan handshake TCP
    dirancang untuk memastikan bahwa sesi dari paket itu sah.<br><br>

    Meskipun sangat hemat sumber daya, firewall ini tidak dapat memeriksa paket yang datang dan pergi. Jadi, jika ada
    sebuah paket mengandung malware, tetapi memiliki handshake TCP yang tepat, paket itu akan lolos. Inilah alasan
    Gateway Level-Sirkuit tidak cukup untuk melindungi komputer yang dipakai untuk bisnis.<br><br>

    <b>3. Stateful Inspection Firewall<br><br></b>

    Stateful Inspection Firewall merupakan jaringan yang bertugas melacak kondisi karakteristik dan pengoperasian
    jaringan. Jenis firewall ini dikonfigurasi untuk membeda-bedakan paket jaringan yang ‘baik’ dan pantas lewat dalam
    berbagai jenis koneksi.<br><br>

    Firewall ini menggabungkan teknologi inspeksi paket dan verifikasi handshake untuk menciptakan tingkat perlindungan
    yang lebih tinggi daripada ketika Anda tidak menggunakannya.<br><br>

    Namun, jenis firewall ini memberikan lebih banyak tekanan pada sumber daya komputasi. Sehingga, dapat memperlambat
    transfer paket yang sah dibandingkan dengan firewall lainnya.<br><br>

    <b>4. Proxy Firewall<br><br></b>

    Proxy Firewall sebenarnya sama saja dengan Application-Level Gateway atau Cloud Firewall. Firewall ini beroperasi
    pada lapisan aplikasi untuk memfilter lalu lintas masuk antara jaringan Anda dan sumber lalu lintas.<br><br>

    Firewall ini dikirimkan melalui solusi berbasis cloud atau perangkat proxy lain. Tidak membiarkan lalu lintas
    terhubung secara langsung, proxy firewall terlebih dahulu membuat koneksi ke sumber lalu lintas dan memeriksa paket
    data yang masuk, jadi benar-benar tersaring dengan baik.<br><br>

    Cara kerjanya sebenarnya mirip dengan Stateful Inspection Firewall karena memeriksa paket dan protokol handshake
    TCP. Namun, proxy firewall juga melakukan inspeksi lapisan dalam paket, memeriksa isi konten dari paket informasi
    untuk memverifikasi bahwa paket tersebut tidak mengandung malware.<br><br>

    Singkatnya, proxy firewall menciptakan lapisan pemisahan ekstra antara klien (sistem tempat paket data berasal) dan
    masing-masing perangkat di jaringan Anda. Memberikan perlindungan tambahan untuk jaringan secara anonim.<br><br>

    Satu kelemahan pada proxy firewall adalah dapat melambatkan jaringan komputer secara signifikan karena ada
    langkah-langkah tambahan dalam proses pengiriman paket datanya.<br><br>

    <b>5. Next-Generation Firewall <br><br></b>

    Ada banyak produk firewall yang baru-baru ini dirilis dan disebut sebagai Next-Generation Firewall. Beberapa fitur
    umum dari arsitektur Next-Generation Firewall ini ada inspeksi paket dalam (memeriksa isi sebenarnya dari paket
    data), pemeriksaan handshake TCP, dan inspeksi paket tingkat-permukaan. <br><br>

    Bedanya, firewall generasi baru ini bisa mencakup teknologi lainnya, seperti intrusion prevention system (IPS) alias
    sistem pencegahan intrusi yang berfungsi untuk menghentikan serangan terhadap jaringan Anda secara otomatis.
    <br><br>

    <b>6. Software Firewall <br><br></b>

    Selanjutnya, ada Software Firewall. Software Firewall mencakup semua jenis firewall yang diinstal pada perangkat.
    Manfaat jenis firewall ini adalah sangat berguna untuk menciptakan pertahanan secara mendalam dengan mengisolasi
    masing-masing titik akhir jaringan dari dua arah. Kekurangannya, pemeliharaan firewall perangkat lunak individual
    pada perangkat yang berbeda bisa jadi sulit dan memakan waktu. <br><br>

    Selain itu, tidak setiap perangkat di jaringan kompatibel dengan software firewall tunggal, jadi mungkin Anda harus
    menggunakan beberapa perangkat lunak firewall yang berbeda untuk tiap komputer. <br><br>

    <b>7. Hardware Firewall <br><br></b>

    Selanjutnya adalah Hardware Firewall. Jenis firewall ini menggunakan alat fisik yang bertindak dengan cara yang
    mirip dengan router lalu lintas. Cara kerjanya mencegat paket data dan permintaan lalu lintas sebelum mereka
    terhubung ke server jaringan. <br><br>

    Firewall berbasis alat fisik seperti ini unggul pada keamanan perimeter dengan memastikan lalu lintas berbahaya dari
    luar jaringan distop sebelum titik akhir jaringan Anda terpapar risiko. <br><br>

    Namun, ada juga kelemahan utama firewall berbasis perangkat keras. Yaitu mudah diserang oleh ‘orang dalam’. Perlu
    diperhatikan jika kemampuan tiap firewall perangkat keras bervariasi tergantung pembuatnya. Beberapa mungkin
    memiliki kapasitas yang lebih terbatas untuk menangani koneksi simultan daripada yang lain. Jadi, pastikan Anda
    pilih Hardware Firewall dari merk dan tipe yang recommended. <br><br>

    <b>8. Cloud Firewall <br><br></b>

    Terakhir, ada Cloud Firewall alias Firewall-as-a-Service (FaaS). Jenis firewall ini dianggap sama dengan Proxy
    Firewall, karena server cloud sering digunakan dalam pengaturan Proxy Firewall. Meskipun sebenarnya proxy tidak
    harus selalu ada di cloud. <br><br>

    Manfaat firewall berbasis cloud adalah sangat mudah untuk ditambahkan ataupun dikurangi. Ketika kebutuhan Anda
    meningkat, Anda bisa menambahkan kapasitas tambahan ke server cloud untuk memfilter beban lalu lintas yang lebih
    besar. Jadi, sama seperti Hardware Firewall, Cloud Firewall ini unggul pada keamanan perimeter. <br><br>

